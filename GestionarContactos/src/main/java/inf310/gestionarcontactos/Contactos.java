    public Contacto buscarContacto(String nombre){
        // Mostrar ventana de selección de árbol
        String[] opciones = {"Arbol Binario", "AVL", "MVias"};
        String seleccion = (String) JOptionPane.showInputDialog(
            null, 
            "Seleccione el árbol que desea usar para buscar:",
            "Seleccionar Árbol",
            JOptionPane.QUESTION_MESSAGE, 
            null, 
            opciones, 
            opciones[0]
        );

        Contacto dummy = new Contacto(nombre, "");
        
        // Verifica la selección y realiza la búsqueda en el árbol correspondiente
        if ("Arbol Binario".equals(seleccion)) {
            return ArbolBinarioBusqueda.buscar(dummy);
        } else if ("AVL".equals(seleccion)) {
            return AVL.buscar(dummy);
        } else if ("MVias".equals(seleccion)) {
            return ArbolMviasBusqueda.buscar(dummy);
        } else {
            JOptionPane.showMessageDialog(null, "No se seleccionó un árbol válido.", "Error", JOptionPane.ERROR_MESSAGE);
            return null;
        }
    }
    
    // Método para eliminar un contacto usando un árbol seleccionado
    public void eliminarContacto(String nombre) {
        // Mostrar ventana de selección de árbol
        String[] opciones = {"Arbol Binario", "AVL", "MVias"};
        String seleccion = (String) JOptionPane.showInputDialog(
            null, 
            "Seleccione el árbol que desea usar para eliminar:",
            "Seleccionar Árbol",
            JOptionPane.QUESTION_MESSAGE, 
            null, 
            opciones, 
            opciones[0]
        );

        Contacto dummy = new Contacto(nombre, "");
        
        // Verifica la selección y realiza la eliminación en el árbol correspondiente
        if ("Arbol Binario".equals(seleccion)) {
            ArbolBinarioBusqueda.eliminar(dummy);
            JOptionPane.showMessageDialog(null, "Contacto eliminado en Árbol Binario");
        } else if ("AVL".equals(seleccion)) {
            AVL.eliminar(dummy);
            JOptionPane.showMessageDialog(null, "Contacto eliminado en AVL");
        } else if ("MVias".equals(seleccion)) {
            ArbolMviasBusqueda.eliminar(dummy);
            JOptionPane.showMessageDialog(null, "Contacto eliminado en MVias");
        } else {
            JOptionPane.showMessageDialog(null, "No se seleccionó un árbol válido.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

