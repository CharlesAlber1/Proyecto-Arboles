    private void mostrarRecorrido(String tipoRecorrido) {
        String[] opciones = {"Arbol Binario", "AVL", "MVias"};
        String seleccion = (String) JOptionPane.showInputDialog(
            null,
            "Seleccione el árbol para realizar el recorrido " + tipoRecorrido + ":",
            "Seleccionar Árbol",
            JOptionPane.QUESTION_MESSAGE,
            null,
            opciones,
            opciones[0]
        );

        if (seleccion == null) {
            return; // Si no selecciona nada, no hacer nada
        }

        List<Contacto> recorrido = new ArrayList<>();
        switch (seleccion) {
            case "Arbol Binario" -> {
                if ("PreOrden".equals(tipoRecorrido)) {
                    recorrido = ArbolBinarioBusqueda.recorridoEnPreOrden();
                } else if ("InOrden".equals(tipoRecorrido)) {
                    recorrido = ArbolBinarioBusqueda.recorridoEnInOrden();
                } else if ("PostOrden".equals(tipoRecorrido)) {
                    recorrido = ArbolBinarioBusqueda.recorridoEnPostOrden();
                }
            }
            case "AVL" -> {
                if ("PreOrden".equals(tipoRecorrido)) {
                    recorrido = AVL.recorridoEnPreOrden();
                } else if ("InOrden".equals(tipoRecorrido)) {
                    recorrido = AVL.recorridoEnInOrden();
                } else if ("PostOrden".equals(tipoRecorrido)) {
                    recorrido = AVL.recorridoEnPostOrden();
                }
            }
            case "MVias" -> {
                if ("PreOrden".equals(tipoRecorrido)) {
                    recorrido = ArbolMviasBusqueda.recorridoEnPreOrden();
                } else if ("InOrden".equals(tipoRecorrido)) {
                    recorrido = ArbolMviasBusqueda.recorridoEnInOrden();
                } else if ("PostOrden".equals(tipoRecorrido)) {
                    recorrido = ArbolMviasBusqueda.recorridoEnPostOrden();
                }
            }
            default -> {
                JOptionPane.showMessageDialog(null, "Árbol no válido.");
                return;
            }
        }
        // Mostrar el resultado del recorrido en jTextArea1
        StringBuilder resultado = new StringBuilder("Recorrido " + tipoRecorrido + ":\n");
        for (Contacto contacto : recorrido) {
            resultado.append(contacto.getNombre()).append(" - ").append(contacto.getTelefono()).append("\n");
        }
        jTextArea1.setText(resultado.toString());
    }
